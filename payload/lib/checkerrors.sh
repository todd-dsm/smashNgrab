#------------------------------------------------------------------------------
# Function. If status code does not equal 0, display message in red, and exit.
#						$1 = status code
#						$2 = text to display on failure.
#  AUTHORS: Todd E Thomas
#     DATE: 2012/01/08
# MODIFIED:
#------------------------------------------------------------------------------

###----------------------------------------------------------------------------
### VARIABLES
###----------------------------------------------------------------------------


###----------------------------------------------------------------------------
### FUNCTION
###----------------------------------------------------------------------------
check_errs()	{
if [ "$1" -ne '0' ]; then
    echo "$0: exiting... encountered error status code $1"
    echo -en "\033[1;31m$0: $2\033[0m \n"
    exit "$1"
fi
}
