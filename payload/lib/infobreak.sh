###----------------------------------------------------------------------------
### VARIABLES
###----------------------------------------------------------------------------
valDATE="$(date +%F\ %r)"
myFileName="$(basename $0)"


###----------------------------------------------------------------------------
### FUNCTION
###----------------------------------------------------------------------------

#------------------------------------------------------------------------------
# for debugging purposes. Generally used during development.
#------------------------------------------------------------------------------
infobreak()    {
    printf '\n%s\n' "###  $myFileName $valDATE @ Line $1: $2"
}
