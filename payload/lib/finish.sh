###----------------------------------------------------------------------------
### VARIABLES
###----------------------------------------------------------------------------
valDATE="$(date +%F\ %r)"


###----------------------------------------------------------------------------
### FUNCTION
###----------------------------------------------------------------------------
finish()  {
    echo "###"
    echo "###"
    echo "###       $valDATE: Finish $0 script"
    echo "###"
    echo "###"
    exit 0
}
